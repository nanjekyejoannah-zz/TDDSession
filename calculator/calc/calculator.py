

class Calculator(object):
    def add(self, num1,num2):
        result = num1 + num2
        return result

    def multiply(self,nums1,nums2):
        ans = nums1 * nums2
        return ans

    def subtraction(self,number1,number2):
        sss = number1 - number2
        return sss

    def divide(self,n1,n2):
        res = n1 / n2
        return res
