class Calculator():
	def add(self, num1, num2):
		result = num1 + num2
		return result

	def subtract(self, num1, num2):
		difference = num2 - num1
		return difference

	def multiply(self, num1, num2):
		product = num2 * num1
		return product

	def divide(self, num1, num2):
		division = num2 / num1
		return division
